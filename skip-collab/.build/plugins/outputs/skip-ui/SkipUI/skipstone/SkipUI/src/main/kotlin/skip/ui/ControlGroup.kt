// Model `ControlGroupStyle` as a struct. Kotlin does not support static members of protocols
class ControlGroupStyle: RawRepresentable<Int> {
    override val rawValue: Int

    constructor(rawValue: Int) {
        this.rawValue = rawValue
    }

    override fun equals(other: Any?): Boolean {
        if (other !is ControlGroupStyle) return false
        return rawValue == other.rawValue
    }

    companion object {

        val automatic = ControlGroupStyle(rawValue = 0)

        @Deprecated("This API is not yet available in Skip. Consider filing an issue against the owning library at https://github.com/skiptools, or see the library README for information on adding support", level = DeprecationLevel.ERROR)
